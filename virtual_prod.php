							<br>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">CurveMediaAds Previs Supervisor Duane Floch reprised his role as Overall Previs Supervisor on the second film in the Apes franchise, Dawn of the Planet of the Apes (Twentieth Century Fox, Dir Matt Reeves) in the Fall of 2012. Duane and CurveMediaAds Film’s Executive Producer Julian Levi assembled two teams; one working out of CurveMediaAds Santa Monica facility and the other embedded within the production team on location in New Orleans.</p>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">The CurveMediaAds crew focused on lens choices, shot-framing, and depth of field to bring Matt Reeves finely-tuned visual aesthetic. With interaction between pre-vis and the Director being critical, regular meetings in LA, Vancouver and New Orleans took place.</p>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">The scope of previs-work on the film was extensive. In all, over 850 shots went through the previs pipeline between both locations. The shared workflow maintained a cohesive look and feel to the scope of work. Assets – environments, props, characters and animation cycles – were regularly updated and shared from either location and daily phone calls and cineSync sessions between locations was a regular part of the process.</p>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">CurveMediaAds most ambitious sequences were the Colony Attack (150 previs shots; 30 postvis shots and approximately 500 apes), Entering the Dam (30 previs/techvis shots), and Village Chaos (80 previs shots; 35 postvis shots and nearly 300 apes). Each of these sequences was shot on location and required detailed, reality-based pre-vis. The physical constraints of each location played a significant role in how the plates would be acquired.</p>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">By the Spring of 2013 the New Orleans team increased to 16 artists and the Santa Monica team to 12. Second Unit Director Brad Parker joined Duane at CurveMediaAds Santa Monica to begin an intense, collaborative effort on the Colony attack sequence – one of the film’s most complex VFX sequences. This work involved several hundred apes and continued through prep right until days before the shoot began and included several trips to the set in downtown New Orleans.</p>
								<p style="color:white;margin-left:20px;margin-right:20px;text-alignment:justify;">By mid-summer 2013, CurveMediaAds transitioned from previs into postvis. As the plates came in and the turnover and screening schedule became more aggressive, more artists were added and embedded with editorial on the Fox lot following a strategy used on the first film. An additional advantage this time round was the availability of CurveMediaAds Bangalore facility for roto and match-move. It allowed the postvis team at Fox to focus solely on story, animation and compositing. The quick turnaround of notes and access to Matt and the Editorial Staff made for a high-paced work-flow on over 450 shots.</p>

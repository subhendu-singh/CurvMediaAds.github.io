                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Paid search marketing</h2>
                        <p>It only takes a matter of seconds to get a paid search campaign up and running. By following a few prompts, your search ads will be ready to reach thousands of search users across the web. However, optimizing your search campaigns takes a little more time, care and finesse.<p>
						<p>Since it’s the most popular platform, let’s assume you plan to use Google Ads – formerly known as AdWords – to run your paid search strategy. Here are a few steps you can take to increase the effectiveness of your campaigns:</p>
						<h5>Improve your quality score</h5>
						<p>Only Google knows the secret sauce that is the algorithm determining an ad’s quality score. However, Google has provided a few hints. To lower your average bid cost and improve your ad rankings, try A/B testing your ad copy and stick with whichever gets the highest click-through rates. Grouping keywords into separate ad groups may also improve your score. Finally, creating relevant landing pages for your ads encourages longer site visits, which is a signal of high quality.</p>
						<h5>Use location-based bidding</h5>
						<p>After you’ve run a campaign for a period of time, check out your geographic metrics. These will tell you where your visitors live, broken down country, region and city. Use this information to modify your bids to more heavily target areas that have already shown higher conversion rates.</p>

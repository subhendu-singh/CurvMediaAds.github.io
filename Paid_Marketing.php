                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Display marketing</h2>
                        <p>Display networks and paid search use many of the same technologies, so the same advice applies to both strategies.</p>
						<p>If you want to retarget your site visitors as they browse the web, keep your main objective in mind: bringing those people back to your site. You can encourage that behavior in several ways:</p>
						<ul>
							<li><b>Optimize your ads with negative keywords.</b> Ad networks allow you specify the keywords for which you don’t want your ads to appear. For example, specifying “free” as a negative keyword will prevent your ads from appearing on web pages listing free versions of your products.</li>
							<li><b>Remind visitors of your value proposition.</b> You have limited space for copy, so make sure it counts. Reminding visitors of your value proposition can encourage them to return to your site.</li>
							<li><b>Use a call to action.</b> Using action verbs and phrases can increase engagement with your ads. “Click to learn more,” is an example of a simple phrase that can lead to great results.</li>

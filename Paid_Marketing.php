                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Balancing paid and inbound marketing strategies</h2>
                        <p>Organic and paid marketing strategies don’t necessarily need to compete with each other. In fact, most brands use a mix of both strategies to draw customers to their sites.</p>
						<p>Paid strategies are usually recommended for driving traffic to high-value, commercial-intent keywords. Especially during peak seasons, paid traffic can help your brand stand out in a sea of competing messages. Paid marketing can also serve to draw traffic to your web pages while you’re waiting for your organic strategy to mature.</p>
						<p>Essentially, if you have a short timeline with specific goals, a paid strategy is probably your best option. However, if your goal is sustained traffic over a long period of time, an organic, inbound strategy is the better choice. Ultimately, your brand will likely use a fluctuating mix of paid and organic marketing throughout the year as your growth goals change and evolve.</p>

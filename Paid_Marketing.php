                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Influencer marketing</h2>
                        <p>If there’s one thing you take away from this section, it’s that you should always research your influencers. There are plenty of fake influencers online who claim to have a long reach but actually inflate their follower counts. Use the tools listed above to look up influencers you’re thinking about working with.</p>
						<p>Once you choose a legitimate influencer, don’t try to direct every aspect of the relationship. After all, they know their followers best, and they’ll know what type of content is most appealing. Listen to their ideas and keep them in the loop about your campaign goals.</p>
						<p>Influencer marketing may not produce immediate results. It’s important to build a relationship with influencers and give them some time to find the best way to collaborate with your brand.</p>

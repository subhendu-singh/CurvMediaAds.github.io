                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Paid social media marketing</h2>
                        <p>First of all, if your brand isn’t on every social media platform under the sun, don’t panic. Maintaining multiple social media pages can be very time consuming, especially when you have a limited amount of resources.</p>
						<p>Consistency is the name of the game. Maintaining a steady stream of high-quality posts is one of the best things you can do on social media. Therefore, you may want to focus on just one or two platforms.</p>
						<h5>Focus on conversations</h5>
						<p>As with SEM, the positioning of your social ads is determined by an algorithm. Each platform has its own algorithm and they are not made public. However, one thing they all value is audience engagement. Posts that spark conversations among real people are the most valuable.</p>
						<p>With every paid social ad you produce, your goal should be to start a conversation. Pose an interesting question or make a statement that begs users to add their own opinion. Bear in mind you may want to moderate these conversations, which can be resource intensive, but can also encourage further engagement.</p>

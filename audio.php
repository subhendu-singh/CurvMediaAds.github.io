                        <h2 class="g-font-size-32--xs g-font-size-36--sm">Sound multichannel formats</h2>
                        <p>Over the years, movie soundscapes have grown in complexity from mono to stereo and ultimately multichannel configurations. Mono (1 signal) format means whatever you hear in your right earbud, you will hear in the left earbud. Both speakers reproduce the exact same thing and you locate the sound from a single position. With stereo (2 signals) or different multichannel audio formats, you start locating the direction of the sonic object. For example, you can locate the singer on your left and the piano on the right, or a helicopter moving from rear to front in a movie. In audio, the ability to locate the direction where the sound comes from in width and depth is called spatialization.</p>
						<p>Multichannel formats used by movie sound engineers include:</p>
						<ul>
							<li>5.1 (Dolby Digital, DTS and THX), the first commercialized home theatre format.</li>
							<li>7.1 (Dolby Pro Logic IIz and DTS NEO:X), which added two rear signals to the 5.1 formats.</li>
							<li>3D audio (Auro 3D / Dolby Atmos / DTSX), initially created for entertainment areas and video games, it added the vertical dimension of sound (signals from above and underneath).</li>
						</ul>
						<p>In nature, there is no such thing as a 5.1 format. Sound doesn’t come from only 5 directions but from everywhere and a 5.1 doesn’t offer enough positions to create a realistic sonic atmosphere. With this in mind, Goldmund developed the Proteus. Proteus bases on 5.1/7.1/… soundtrack formats and delivers a 3D immersion with no need for ceiling speakers (as opposed to 3D formats).</p>
